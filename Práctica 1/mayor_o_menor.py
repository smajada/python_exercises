# Leer dos números que llamaremos A y B
A = int(input("Introduce un primer número: "))
B = int(input("Introduce un segundo número: "))

# Si A es mayor que B
if A > B:

    # Imprimir "A es mayor que B"
    print(A, "es mayor")

# Si no
else:

    # Imprimir "B es mayor que A"
    print(B, "es mayor")

